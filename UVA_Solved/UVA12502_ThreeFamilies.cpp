#include <iostream>
#include <stdio.h>
using namespace std;

int main() 
{
	int result, testCase, Payment, WorkHourA, WorkHourB, temp;
	scanf("%d", &testCase);
	for(int i = 0;i < testCase;i++)
	{
		scanf("%d %d %d", &WorkHourA, &WorkHourB, &Payment);
		temp = (WorkHourA + WorkHourB);
		WorkHourA = WorkHourA + (WorkHourA - WorkHourB);
		result = Payment * WorkHourA;
		result /= temp;
		if(result < 0)
			result = 0;
		cout << result << endl;
	}
	return 0;
}